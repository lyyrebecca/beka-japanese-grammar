function normalizeReadablePattern(pattern) {
  return String(pattern || "")
    .replace(/[「」『』（）()【】\[\]\s　]/g, "")
    .replace(/\.\.\.|〜|~|…/g, "")
    .split(/[\/／・、,]/)[0]
    .toLowerCase();
}

function readablePatternKeys(pattern) {
  const raw = String(pattern || "").replace(/[「」『』（）()【】\[\]\s　]/g, "");
  const parts = raw.split(/[\/／・、,]/).filter(Boolean);
  return (parts.length ? parts : [raw]).map(normalizeReadablePattern);
}

function upgradeChallengeLibrary() {
  for (const group of GRAMMAR_GROUPS) {
    const enriched = group.challenges.map((item, index) => enrichChallenge(group, item, index, "精选题"));
    const covered = new Set(enriched.map((item) => normalizePatternKey(item[1])));
    const coveredLevels = new Set(enriched.map((item) => levelFromText(item.level)).filter(Boolean));
    const expressionLevels = [...new Set(group.expressions.map((item) => levelFromText(item.level)).filter(Boolean))]
      .sort((a, b) => levelRank(a) - levelRank(b));
    const candidates = group.expressions
      .filter((item) => item.pattern && !covered.has(normalizePatternKey(item.pattern)))
      .sort((a, b) => levelRank(a.level) - levelRank(b.level));
    const targetCount = Math.min(20, Math.max(12, group.expressions.length));

    for (const level of expressionLevels) {
      if (coveredLevels.has(level)) continue;
      const expressionItem = candidates.find((item) => levelFromText(item.level) === level);
      if (!expressionItem) continue;
      enriched.push(challengeFromExpression(group, expressionItem, enriched.length));
      covered.add(normalizePatternKey(expressionItem.pattern));
      coveredLevels.add(level);
    }

    for (const expressionItem of candidates) {
      if (enriched.length >= targetCount) break;
      if (covered.has(normalizePatternKey(expressionItem.pattern))) continue;
      enriched.push(challengeFromExpression(group, expressionItem, enriched.length));
      covered.add(normalizePatternKey(expressionItem.pattern));
    }

    group.challenges = enriched.sort((a, b) => levelRank(a.level) - levelRank(b.level));
  }
}

function enrichChallenge(group, item, index, sourceType) {
  const target = item[1] || "";
  const expressionItem = findExpressionForTarget(group, target);
  const level = item.level || expressionItem?.level || levelFromText(target) || "综合";
  const sourceBook = item.sourceBook || expressionItem?.sourceBook || "新完全掌握日语语法书 N4-N1";
  const sourceLesson = item.sourceLesson || expressionItem?.sourceLesson || "翻译练习改写";
  item.level = level;
  item.sourceBook = sourceBook;
  item.sourceLesson = sourceLesson;
  item.sourceType = item.sourceType || sourceType;
  item.id = item.id || `${group.id}-challenge-${index}-${normalizePatternKey(target).slice(0, 32)}`;
  item.keywords = item[2];
  return item;
}

function challengeFromExpression(group, item, index) {
  const hasRealExample = isUsefulPracticeExample(item);
  const prompt = hasRealExample
    ? cleanPracticeText(item.translation)
    : fallbackPracticePrompt(group, item);
  const sample = hasRealExample
    ? cleanPracticeText(item.example)
    : fallbackPracticeAnswer(item);
  const keywords = keywordHintsForExpression(item);
  const sourceBook = item.sourceBook || `新完全掌握日语语法书 ${levelFromText(item.level) || "N4-N1"}`;
  const sourceLesson = item.sourceLesson || "语法知识点改写练习";
  return challenge(
    prompt,
    item.pattern,
    keywords,
    sample,
    `${levelFromText(item.level) || item.level} 练习：先确认接续「${item.connection || "见知识卡片"}」，再体会语感：${item.nuance || item.meaning}。参考：${sourceBook.replace("新完全掌握日语语法书 ", "新完全掌握 ")} ${sourceLesson}`,
    {
      id: `${group.id}-auto-${index}-${normalizePatternKey(item.pattern).slice(0, 32)}`,
      level: item.level,
      sourceBook,
      sourceLesson,
      sourceType: hasRealExample ? "例句改写" : "知识点造句"
    }
  );
}

function findExpressionForTarget(group, target) {
  const targetKey = normalizePatternKey(target);
  const pairs = group.expressions.map((item) => ({ item, key: normalizePatternKey(item.pattern) }));
  return pairs.find((pair) => pair.key === targetKey)?.item
    || pairs
      .filter((pair) => pair.key.includes(targetKey) || targetKey.includes(pair.key))
      .sort((a, b) => b.key.length - a.key.length)[0]?.item;
}

function isUsefulPracticeExample(item) {
  const text = `${item.example || ""} ${item.translation || ""}`;
  return Boolean(item.example && item.translation && !/この文型|確認这个文型|表达例句/.test(text));
}

function cleanPracticeText(text) {
  return String(text || "")
    .replace(/<ruby>(.*?)<rt>.*?<\/rt><\/ruby>/g, "$1")
    .replace(/<[^>]+>/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function fallbackPracticePrompt(group, item) {
  const level = levelFromText(item.level);
  const theme = group.theme || "语法";
  const base = {
    N4: `请用「${item.pattern}」翻译：我想把${theme}表达说得更准确。`,
    N3: `请用「${item.pattern}」翻译：在这种情况下，要注意${item.meaning || theme}的语感。`,
    N2: `请用「${item.pattern}」翻译：根据实际情况，我们需要正式说明${item.meaning || theme}。`,
    N1: `请用「${item.pattern}」翻译：从整体来看，这正体现了${item.meaning || theme}这一点。`
  };
  return base[level] || `请用「${item.pattern}」翻译：请根据语境表达“${item.meaning || theme}”。`;
}

function fallbackPracticeAnswer(item) {
  const level = levelFromText(item.level);
  const pattern = displayPatternForSentence(item.pattern);
  if (level === "N1") return `この問題は、${pattern}という観点から考える必要があります。`;
  if (level === "N2") return `状況に応じて、${pattern}を正しく使う必要があります。`;
  if (level === "N3") return `この場面では、${pattern}を使うと自然に表現できます。`;
  return `日本語では、${pattern}を正しく使いたいです。`;
}

function displayPatternForSentence(pattern) {
  return String(pattern || "")
    .split(/[\/／・]/)[0]
    .replace(/[A-Zａ-ｚ]\s*/g, "")
    .replace(/\.\.\.|〜|~|…/g, "")
    .replace(/[（）()].*?[）)]/g, "")
    .trim() || pattern;
}

function keywordHintsForExpression(item) {
  const variants = patternVariants(item.pattern);
  const core = variants.length > 1 ? [variants.slice(0, 4)] : variants.slice(0, 1);
  const sourceWords = cleanPracticeText(item.example).match(/[一-龯ぁ-んァ-ヶー]{2,}/g) || [];
  const extra = sourceWords
    .filter((word) => !variants.some((variant) => variant.includes(word) || word.includes(variant)))
    .slice(0, 2);
  return [...core, ...extra].filter(Boolean);
}

function patternVariants(pattern) {
  return [...new Set(String(pattern || "")
    .split(/[\/／・、,]/)
    .map((part) => part
      .replace(/[（）()].*?[）)]/g, "")
      .replace(/\.\.\.|〜|~|…/g, "")
      .replace(/\s+/g, "")
      .trim())
    .filter((part) => part.length >= 2))];
}

function levelRank(level) {
  const levelText = String(level || "");
  if (levelText.includes("N5")) return 0;
  if (levelText.includes("N4")) return 1;
  if (levelText.includes("N3")) return 2;
  if (levelText.includes("N2")) return 3;
  if (levelText.includes("N1")) return 4;
  return 5;
}

function levelFromText(text) {
  const match = String(text || "").match(/N[1-5]/);
  return match ? match[0] : "";
}
