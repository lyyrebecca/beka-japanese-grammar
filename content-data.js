function improveExampleLibrary() {
  for (const group of GRAMMAR_GROUPS) {
    for (const item of group.expressions) {
      const sample = naturalExampleForExpression(item, group.theme);
      if (needsBetterExample(item) || shouldUseSpecificExample(item, sample)) {
        item.example = sample.example;
        item.translation = sample.translation;
        item.exampleSourceType = sample.exampleSourceType;
      }
      item.exampleQuality = needsBetterExample(item) ? "needs-review" : "checked";
      item.exampleSourceType ||= sample.exampleSourceType || "既存例句";
    }
  }
}

function shouldUseSpecificExample(item, sample) {
  if (!sample?.example || !sample?.translation) return false;
  if (!exampleContainsPatternCore(item.example, item.pattern) && exampleContainsPatternCore(sample.example, item.pattern)) return true;
  return false;
}

function exampleContainsPatternCore(example, pattern) {
  const text = cleanPracticeText(example).replace(/\s+/g, "");
  return patternVariants(pattern).some((variant) => {
    const core = String(variant || "")
      .replace(/[A-Zａ-ｚ]/g, "")
      .replace(/[（）()].*?[）)]/g, "")
      .replace(/\s+/g, "");
    if (!core || core.length < 2) return true;
    return text.includes(core);
  });
}

function needsBetterExample(item) {
  const text = `${item.example || ""} ${item.translation || ""}`;
  return !item.example
    || !item.translation
    || /この文型|確認这个文型|表达例句|日本語では|使いたいです|自然に表現できます|状況に応じて|観点から考える必要があります/.test(text)
    || /「[^」]+」を確認|「[^」]+」を使/.test(text);
}

function naturalExampleForExpression(item = {}, theme = "语法") {
  const pattern = String(item.pattern || "");
  if (pattern === "の / こと") {
    return { example: "毎日少しずつ続けることが大切です。", translation: "每天一点点坚持很重要。", exampleSourceType: "原书语境改写" };
  }
  if (pattern === "こ・そ・あ") {
    return { example: "この本はここに、その本はそこに置いてください。", translation: "这本书请放在这里，那本书请放在那里。", exampleSourceType: "原书语境改写" };
  }
  const direct = specificExample(pattern);
  if (direct) return { ...direct, exampleSourceType: direct.exampleSourceType || "原书例句/低噪声改写" };
  return { ...fallbackNaturalExample(item, theme), exampleSourceType: "原书语境改写" };
}

