function buildGroup(id, title, theme, summary, expressionRows, prompts) {
  const expressions = expressionRows.map(([level, pattern, meaning, connection, nuance]) => {
    const sample = naturalExampleForExpression({ level, pattern, meaning, connection, nuance }, theme);
    return { level, pattern, meaning, connection, nuance, ...sample };
  });
  return {
    id,
    title,
    theme,
    summary,
    expressions,
    notes: [
      `${expressions[0].pattern} 是基础表达，先保证接续准确。`,
      `${expressions[1].pattern} 常用于把意思说得更具体。`,
      `${expressions[2].pattern} 往往比基础表达更带语感或判断。`,
      `${expressions[3].pattern} 偏 N2 或书面表达，注意场景。`
    ],
    challenges: prompts.map((prompt, index) => {
      const exp = expressions[index % expressions.length];
      return [
        prompt,
        exp.pattern,
        keywordHints(exp.pattern),
        sampleAnswer(prompt, exp.pattern),
        `这一题目标是练 ${exp.pattern}。先检查接续，再看语气是否符合“${exp.nuance}”。`
      ];
    }),
    rewrites: [
      [`请用基础表达说：${prompts[0]}`, `换成 ${expressions[1].pattern}`, sampleAnswer(prompts[0], expressions[1].pattern)],
      [`请用 ${expressions[2].pattern} 改写：${prompts[1]}`, `换成更有语感的表达`, sampleAnswer(prompts[1], expressions[2].pattern)]
    ]
  };
}

function makeExample(theme, pattern, index) {
  return naturalExampleForExpression({ pattern }, theme).example;
}

function keywordHints(pattern) {
  return pattern
    .replace(/[（）]/g, " ")
    .split(/[ /・]+/)
    .filter(Boolean)
    .slice(0, 3);
}

function sampleAnswer(prompt, pattern) {
  if (SAMPLE_ANSWER_BANK[prompt]) return SAMPLE_ANSWER_BANK[prompt];
  const clean = pattern.split(/[ /・]/)[0].replace(/（.*?）/g, "");
  return `例：${prompt} → この内容は「${clean}」を使って自然な日本語にします。`;
}

